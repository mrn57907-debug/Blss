  function _renderCat(catIdx) {
    const body = document.getElementById("epBody");
    if (!body) return;
    const catName = categories[catIdx];
    let emojis = catIdx === 0 ? _getRecents() : EMOJI_DATA[catName];
    if (!emojis || emojis.length === 0) {
      body.innerHTML = `<div class="ep-no-results">${catIdx === 0 ? "Щ„Ш§ ЩҠЩҲШ¬ШҜ ШҘЩҠЩ…ЩҲШ¬ЩҠ Щ…ШіШӘШ®ШҜЩ… ШЁШ№ШҜ" : "Щ„Ш§ ЩҠЩҲШ¬ШҜ ШҘЩҠЩ…ЩҲШ¬ЩҠ"}</div>`;
      return;
    }
    body.innerHTML = `<div class="ep-section-label">${catName}</div><div class="ep-grid">${emojis.map(e=>`<button class="ep-emoji-btn" data-emoji="${e}" title="${e}">${e}</button>`).join("")}</div>`;
    body.scrollTop = 0;
  }

  function _onSearch(e) {
    const q = e.target.value.trim().toLowerCase();
    const body = document.getElementById("epBody");
    if (!body) return;
    if (!q) { _renderCat(_currentCat); return; }
    // Search across all categories except Recent
    const all = [];
    categories.slice(1).forEach(cat => { all.push(...EMOJI_DATA[cat]); });
    // Simple: filter by unicode name lookup вҖ” just show all that include query char or filter first 200
    // Since we don't have name DB, return all and let user scroll вҖ” search by emoji char match
    const matched = all.filter(em => em.includes(q) || [...em].some(c => c.codePointAt(0)?.toString(16).includes(q)));
    if (matched.length === 0) { body.innerHTML = `<div class="ep-no-results">Щ„Ш§ ШӘЩҲШ¬ШҜ ЩҶШӘШ§ШҰШ¬ Щ„ЩҖ "${q}"</div>`; return; }
    body.innerHTML = `<div class="ep-section-label">ЩҶШӘШ§ШҰШ¬ Ш§Щ„ШЁШӯШ«</div><div class="ep-grid">${matched.slice(0,80).map(em=>`<button class="ep-emoji-btn" data-emoji="${em}" title="${em}">${em}</button>`).join("")}</div>`;
    body.scrollTop = 0;
  }

  function _insertEmoji(emoji) {
    if (!emoji) return;
    _addRecent(emoji);
    const inp = _inp();
    if (!inp) return;
    const start = inp.selectionStart ?? inp.value.length;
    const end   = inp.selectionEnd   ?? inp.value.length;
    inp.value = inp.value.slice(0, start) + emoji + inp.value.slice(end);
    const pos = start + [...emoji].length;
    inp.focus();
    try { inp.setSelectionRange(pos, pos); } catch(e){}
    inp.dispatchEvent(new Event("input", { bubbles: true }));
    if (window.innerWidth < 600) _close();
  }

  function _positionPicker() {
    const vw = window.innerWidth, vh = window.innerHeight;
    const pw = 320, ph = 420;
    const bR = btn.getBoundingClientRect();
    // ШҘШ°Ш§ Ш§Щ„ШІШұ Щ…Ш®ЩҒЩҠ ШЈЩҲ Ш®Ш§ШұШ¬ Ш§Щ„ШҙШ§ШҙШ© (width=0 ШЈЩҲ top=0 ШЁШіШЁШЁ display:none ЩҒЩҠ parent)
    const btnVisible = bR.width > 0 && bR.height > 0 && bR.bottom > 0;
    let top, left;
    if (btnVisible) {
      top  = bR.top - ph - 8;
      left = bR.left + bR.width / 2 - pw / 2;
      if (top < 8) top = bR.bottom + 8;
    } else {
      // fallback: ШұЩғЩҶ ШЈШіЩҒЩ„ Ш§Щ„ШҙШ§ШҙШ©
      top  = vh - ph - 8;
      left = vw / 2 - pw / 2;
    }
    if (top + ph > vh - 8) top = vh - ph - 8;
    if (top < 8) top = 8;
    if (left + pw > vw - 8) left = vw - pw - 8;
    if (left < 8) left = 8;
    wrap.style.top  = top  + "px";
    wrap.style.left = left + "px";
    wrap.style.width = Math.min(pw, vw - 16) + "px";
  }

